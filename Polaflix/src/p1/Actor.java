package p1;
/**
 * Clase que implementa un actor
 */
public class Actor extends Persona
{
	/**
	 * Constructor vacío
	 */
	public Actor() {
		super();
	}

	/**
	 * Constructor con parámetros
	 * @param nombre
	 * @param apellido
	 */
	public Actor(String nombre, String apellido) {
		super(nombre, apellido);
	}	
}
